console.log('===increment===')
var j = 0
var k = 10
var j = k++
console.log(j)
console.log(k + 1)

var i = 0
var h = 10
var i = ++h
console.log(i + 1)
console.log(h + 1)

console.log('===decrement===')
var l = 0
var m = 10
var l = m--
console.log(l)
console.log(m - 1)

var o = 0
var p = 10
var o = --p
console.log(o - 1)
console.log(p - 1)
